package dev.webfx.platform.visibility;

/**
 * Represents the visibility state of the application.
 *
 * @author Bruno Salmon
 */
public enum VisibilityState {
    /**
     * Indicates that the application is currently visible to the user.
     */
    VISIBLE,

    /**
     * Indicates that the application is currently hidden from the user
     */
    HIDDEN
}
